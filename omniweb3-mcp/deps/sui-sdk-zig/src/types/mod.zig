test "address hex roundtrip" {
    const hex = "0x000102030405060708090a0b0c0d0e0f101112131415161718191a1b1c1d1e1f";
    const addr = try Address.parseHex(hex);
    var buffer: [66]u8 = undefined;
    const rendered = try addr.toHexLower(&buffer);
    try std.testing.expectEqualStrings(hex, rendered);
}
